package client.classes;

import java.io.Serializable;
import java.util.Date;

/**
 * Service Informations.
 * 
 * @author gassen
 */
public class Service implements Serializable {
    /** Version Id. */
    private static final long serialVersionUID = -1695708581451374349L;
    /** Service Start Time. */
    private Date startTime;
    /** Service Duration. */
    private int duration;
    /** Source Endpoint Id. */
    private String source;
    /** Destination Endpoint Id. */
    private String destination;
    /** Service Status. */
    private String status;
    /** Min Bandwith. */
    private int minBw;
    /** Max Bandwith. */
    private int maxBw;
    /** max Delay. */
    private int maxDelay;
    /** Service Id. */
    private long id;

    /**
     * Default Constructor.
     */
    public Service() {
        super();
        // TODO Auto-generated constructor stub
    }

    /**
     * Get Service Start Time.
     * 
     * @return Service Start Time.
     */
    public final Date getStartTime() {
        return this.startTime;
    }

    /**
     * Set Service Start Time.
     * 
     * @param startTime
     *            Service Start Time.
     */
    public final void setStartTime(final Date startTime) {
        this.startTime = startTime;
    }

    /**
     * Get Service Duration.
     * 
     * @return Service Duration.
     */
    public final int getDuration() {
        return this.duration;
    }

    /**
     * Set Service Duration.
     * 
     * @param duration
     *            Service Duration.
     */
    public final void setDuration(final int duration) {
        this.duration = duration;
    }

    /**
     * Get Service Source Endpoint Id.
     * 
     * @return Service Source Endpoint Id.
     */
    public final String getSource() {
        return this.source;
    }

    /**
     * Set Service Source Endpoint Id.
     * 
     * @param source
     *            Service Source Endpoint Id.
     */
    public final void setSource(final String source) {
        this.source = source;
    }

    /**
     * Get Service Destination Endpoint Id.
     * 
     * @return Service Destination Endpoint Id.
     */
    public final String getDestination() {
        return this.destination;
    }

    /**
     * Set Service Destination Endpoint Id.
     * 
     * @param destination
     *            Service Destination Endpoint Id.
     */
    public final void setDestination(final String destination) {
        this.destination = destination;
    }

    /**
     * Get Service Status.
     * 
     * @return Service Status.
     */
    public final String getStatus() {
        return this.status;
    }

    /**
     * Set Service Status.
     * 
     * @param status
     *            Service Status.
     */
    public final void setStatus(final String status) {
        this.status = status;
    }

    /**
     * Get Service Id.
     * 
     * @return Service Id.
     */
    public final long getId() {
        return this.id;
    }

    /**
     * Set Service Id.
     * 
     * @param id
     *            Service Id.
     */
    public final void setId(final long id) {
        this.id = id;
    }

    /**
     * Get Min BW.
     * 
     * @return the minBw
     */
    public final int getMinBw() {
        return this.minBw;
    }

    /**
     * Set Min BW.
     * 
     * @param minBw
     *            the minBw to set
     */
    public final void setMinBw(final int minBw) {
        this.minBw = minBw;
    }

    /**
     * Get Max BW.
     * 
     * @return the maxBw
     */
    public final int getMaxBw() {
        return this.maxBw;
    }

    /**
     * Set Max BW.
     * 
     * @param maxBw
     *            the maxBw to set
     */
    public final void setMaxBw(final int maxBw) {
        this.maxBw = maxBw;
    }

    /**
     * Get maximum delay.
     * 
     * @return the maxDelay
     */
    public final int getMaxDelay() {
        return this.maxDelay;
    }

    /**
     * Set maximum delay.
     * 
     * @param maxDelay
     *            the maxDelay to set
     */
    public final void setMaxDelay(final int maxDelay) {
        this.maxDelay = maxDelay;
    }
}
